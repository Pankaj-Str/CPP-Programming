#include<iostream>

using namespace std;

int main(){

    // Datatype int 
    int a = 10;
    cout<<"a: "<<a<<endl;

    // Datatype float
    float b = 20.5;
    cout<<"b: "<<b<<endl;

    // Datatype double

    double c = 30.5;
    cout<<"c: "<<c<<endl;

    // Datatype char

    char d = 'A';
    cout<<"d: "<<d<<endl;

    // Datatype string

    string e = "Hello World!";
    cout<<"e: "<<e<<endl;


    // Datatype bool

    bool f = true;
    cout<<"f: "<<f<<endl;

    // Datatype long

    long g = 1000000000;

    cout<<"g: "<<g<<endl;


    // Datatype short

    short h = 10;

    cout<<"h: "<<h<<endl;

    
    // Datatype  long long
    long long i = 1000000000000000000;

    cout<<"i: "<<i<<endl;

    // Datatype unsigned int

    unsigned int j = 10;

    cout<<"j: "<<j<<endl;

    // Datatype unsigned long

    unsigned long k = 1000000000;

    cout<<"k: "<<k<<endl;

    // Datatype unsigned long long

    unsigned long long l = 1000000000000000000;

    cout<<"l: "<<l<<endl;

    // Datatype unsigned short

    unsigned short m = 10;

    cout<<"m: "<<m<<endl;

    // Datatype unsigned char
    unsigned char n = 'A';

    cout<<"n: "<<n<<endl;



    return 0;
}
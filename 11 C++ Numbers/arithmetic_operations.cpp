#include <iostream>

int main() {
    // Arithmetic operations
    int num1 = 10;
    int num2 = 5;

    std::cout << "Addition: " << num1 + num2 << std::endl;
    std::cout << "Subtraction: " << num1 - num2 << std::endl;
    std::cout << "Multiplication: " << num1 * num2 << std::endl;
    std::cout << "Division: " << num1 / num2 << std::endl;

    return 0;
}

#include<iostream>
using namespace std;
int main(){
    // while loop example 
    // start point (initialization)
    // end point (condition)
    // output (print statement)
    // increment ++ or decrement -- (update)
    // ---------Explanation---------
    // the loop starts with i = 1
    // it prints of i and increments it in ech iteration
    // the loop stops when i is greater than 10
    // -----------------------------
int i = 1; // initialization

    while(i <= 10){ // condition
        cout << i << endl; // output
        i++; // update

    }
    return 0;
}
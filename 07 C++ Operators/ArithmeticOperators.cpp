#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 5;

    // Addition
    int sum = a + b;
    cout << "Sum: " << sum << endl;

    // Subtraction
    int difference = a - b;
    cout << "Difference: " << difference << endl;

    // Multiplication
    int product = a * b;
    cout << "Product: " << product << endl;

    // Division
    int quotient = a / b;
    cout << "Quotient: " << quotient << endl;

    // Modulus (remainder)
    int remainder = a % b;
    cout << "Remainder: " << remainder << endl;

    return 0;
}
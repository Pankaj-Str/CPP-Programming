#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 5;

    // Equal to
    cout << (a == b) << endl;

    // Not equal to
    cout << (a != b) << endl;

    // Greater than
    cout << (a > b) << endl;

    // Less than
    cout << (a < b) << endl;

    // Greater than or equal to
    cout << (a >= b) << endl;

    // Less than or equal to
    cout << (a <= b) << endl;

    return 0;
}
#include<iostream>

using namespace std;

int main(){
    // Integer data type
    int myint = 20;

    cout<<"Int = "<<myint<<endl;
 
    // Floating-point data types
    double myDouble = 3.14;
    float myFloat = 2.718f; // 'f' suffix denotes a float

    cout<<"double = "<<myDouble<<endl;
    cout<<"float = "<<myFloat<<endl;

    // Character data type
    char myChar = 'A';
    cout<<"Char = "<<myChar<<endl;
    // Boolean data type
    bool myBool = false;
    cout<<"Bool = "<<myBool<<endl;
     // String data type from the C++ Standard Library
     string name = "joy";

    cout<<"String = "<<name<<endl;

    return 0;
}